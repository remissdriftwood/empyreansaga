const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');
const AdmZip = require('adm-zip');

// --- User Paths ---
const DOWNLOADS_DIR = 'C:\\Users\\burr\\Downloads';
const TOOLS_DIR = 'C:\\Users\\burr\\Documents\\RMMZ\\Empyrean Saga\\js\\plugins\\empyreansaga\\tools\\dialogue-parser';
const DATA_DIR = 'C:\\Users\\burr\\Documents\\RMMZ\\Empyrean Saga\\data';

console.log("=========================================");
console.log("🚀 Starting Empyrean Dialogue Pipeline...");
console.log("=========================================\n");

try {
    // 1. Find all recent zip files in Downloads
    const files = fs.readdirSync(DOWNLOADS_DIR)
        .filter(file => file.endsWith('.zip') && file.includes('Export'))
        .map(file => ({
            name: file,
            path: path.join(DOWNLOADS_DIR, file),
            time: fs.statSync(path.join(DOWNLOADS_DIR, file)).mtime.getTime()
        }))
        .sort((a, b) => b.time - a.time); // Sort newest to oldest

    if (files.length === 0) {
        throw new Error("Could not find any Notion Export .zip files in Downloads.");
    }

    // 2. Read the ZIPs until we find both files
    let foundEntities = false;
    let foundDialogue = false;
    let zipsProcessed = []; // Keep track so we can delete them later

    console.log(`[1/5] Scanning recent exports for CSVs...`);

    // Recursive extraction function
    function extractFromZip(zipInput) {
        const zip = new AdmZip(zipInput);
        const zipEntries = zip.getEntries();

        zipEntries.forEach(entry => {
            if (entry.isDirectory) return;

            const fileName = entry.name || entry.entryName;

            if (fileName.endsWith('.zip')) {
                // Nested zip!
                extractFromZip(entry.getData());
            } else if (!foundEntities && fileName.includes('Entities') && fileName.includes('_all.csv')) {
                fs.writeFileSync(path.join(TOOLS_DIR, 'Entities.csv'), entry.getData());
                foundEntities = true;
                console.log(`         ✅ Extracted: Entities.csv`);
            } else if (!foundDialogue && fileName.includes('Dialogue') && fileName.includes('_all.csv')) {
                fs.writeFileSync(path.join(TOOLS_DIR, 'Dialogue.csv'), entry.getData());
                foundDialogue = true;
                console.log(`         ✅ Extracted: Dialogue.csv`);
            }
        });
    }

    // Loop through the downloaded zips starting with the newest
    for (const fileMeta of files) {
        if (foundEntities && foundDialogue) break; // Stop if we found both!

        console.log(`  -> Checking archive: ${fileMeta.name}`);
        zipsProcessed.push(fileMeta.path);
        extractFromZip(fileMeta.path);
    }

    if (!foundEntities || !foundDialogue) {
        let missing = [];
        if (!foundEntities) missing.push("Entities..._all.csv");
        if (!foundDialogue) missing.push("Dialogue..._all.csv");
        throw new Error(`Could not find the following files in your recent exports: ${missing.join(', ')}. Did you forget to export one of the databases?`);
    }

    // 3. Run the existing parser script
    console.log(`\n[3/5] Running build-dialogue.js...`);
    execSync('node build-dialogue.js', { cwd: TOOLS_DIR, stdio: 'inherit' });

    // 4. Move the generated JSON to the MZ Data folder
    const sourceJson = path.join(TOOLS_DIR, 'Dialogue.json');
    const targetJson = path.join(DATA_DIR, 'Dialogue.json');

    if (fs.existsSync(sourceJson)) {
        fs.copyFileSync(sourceJson, targetJson);
        console.log(`\n[4/5] Successfully copied Dialogue.json to MZ Data folder.`);
    } else {
        throw new Error("Dialogue.json was not generated by the builder!");
    }

    // 5. Cleanup
    console.log(`[5/5] Cleaning up workspace...`);
    
    // Delete all the root zips we had to open
    for (const zipPath of zipsProcessed) {
        if (fs.existsSync(zipPath)) fs.unlinkSync(zipPath);
    }
    
    // Delete temporary CSVs
    if (fs.existsSync(path.join(TOOLS_DIR, 'Entities.csv'))) fs.unlinkSync(path.join(TOOLS_DIR, 'Entities.csv'));
    if (fs.existsSync(path.join(TOOLS_DIR, 'Dialogue.csv'))) fs.unlinkSync(path.join(TOOLS_DIR, 'Dialogue.csv'));
    console.log(`      -> Deleted source Zips and temporary CSVs.`);

    console.log("\n=========================================");
    console.log("✨ Pipeline Complete! MZ is ready to play.");
    console.log("=========================================\n");

} catch (error) {
    console.error("\n❌ [PIPELINE ERROR]", error.message);
}